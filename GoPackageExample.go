package gopackageexample

func HelloWorld() {
	println("Hello World")
}

func Greeting(name string) string {
	return "Hello " + name
}

func Add(x int, y int) int {
	return x + y
}

func Sub(x int, y int) int {
	return x - y
}

func Mul(x int, y int) int {
	return x * y
}

func Div(x int, y int) int {
	return x / y
}
